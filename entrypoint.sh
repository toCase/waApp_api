#!/bin/bash
set -e

echo "üîÑ –ó–∞–ø—É—Å–∫ Django –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è..."

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î
check_db() {
    python -c "
import django
import os
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'apicore.settings')
django.setup()
from django.db import connection
try:
    connection.ensure_connection()
    print('‚úÖ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –¥–æ—Å—Ç—É–ø–Ω–∞')
    exit(0)
except Exception as e:
    print(f'‚ùå –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞: {e}')
    exit(1)
"
}

# –û–∂–∏–¥–∞–Ω–∏–µ –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç–∏ –ë–î (–º–∞–∫—Å–∏–º—É–º 60 —Å–µ–∫—É–Ω–¥)
echo "‚è≥ –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ—Å—Ç—É–ø–Ω–æ—Å—Ç—å –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö..."
retry_count=0
max_retries=60

while ! check_db; do
    retry_count=$((retry_count + 1))
    if [ $retry_count -eq $max_retries ]; then
        echo "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö –∑–∞ 60 —Å–µ–∫—É–Ω–¥"
        exit 1
    fi
    echo "‚è≥ –û–∂–∏–¥–∞–Ω–∏–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –ë–î... (–ø–æ–ø—ã—Ç–∫–∞ $retry_count/$max_retries)"
    sleep 1
done

# –°–æ–∑–¥–∞–Ω–∏–µ –º–∏–≥—Ä–∞—Ü–∏–π (–µ—Å–ª–∏ –µ—Å—Ç—å –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –º–æ–¥–µ–ª—è—Ö)
echo "üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –º–æ–¥–µ–ª—è—Ö..."
python manage.py makemigrations --dry-run --verbosity=1 | grep -q "No changes detected" || {
    echo "üìù –û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤ –º–æ–¥–µ–ª—è—Ö, —Å–æ–∑–¥–∞—ë–º –º–∏–≥—Ä–∞—Ü–∏–∏..."
    python manage.py makemigrations --verbosity=2
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –Ω–µ–ø—Ä–∏–º–µ–Ω—ë–Ω–Ω—ã—Ö –º–∏–≥—Ä–∞—Ü–∏–π
echo "üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–µ–ø—Ä–∏–º–µ–Ω—ë–Ω–Ω—ã–µ –º–∏–≥—Ä–∞—Ü–∏–∏..."
if python manage.py showmigrations --plan | grep -q "\[ \]"; then
    echo "üîÑ –ü—Ä–∏–º–µ–Ω—è–µ–º –º–∏–≥—Ä–∞—Ü–∏–∏..."
    python manage.py migrate --verbosity=2
    echo "‚úÖ –ú–∏–≥—Ä–∞—Ü–∏–∏ —É—Å–ø–µ—à–Ω–æ –ø—Ä–∏–º–µ–Ω–µ–Ω—ã"
else
    echo "‚úÖ –í—Å–µ –º–∏–≥—Ä–∞—Ü–∏–∏ —É–∂–µ –ø—Ä–∏–º–µ–Ω–µ–Ω—ã"
fi

# –°–±–æ—Ä —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏—Ö —Ñ–∞–π–ª–æ–≤
echo "üì¶ –°–æ–±–∏—Ä–∞–µ–º —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–µ —Ñ–∞–π–ª—ã..."
python manage.py collectstatic --noinput --clear || {
    echo "‚ö†Ô∏è  –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±–æ—Ä–µ —Å—Ç–∞—Ç–∏–∫–∏, –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º..."
}

echo "üöÄ –ó–∞–ø—É—Å–∫–∞–µ–º Django —Å–µ—Ä–≤–µ—Ä..."
exec "$@"